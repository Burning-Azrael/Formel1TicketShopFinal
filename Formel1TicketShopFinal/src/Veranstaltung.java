public class Veranstaltung {
    private int vid;
    private String ort;
    private int ticketanzahl;
    private String ticketoption;
    
    public int getVid() {
        return vid;
    }
    public void setVid(int vid) {
        this.vid = vid;
    }
    public String getOrt() {
        return ort;
    }
    public void setOrt(String ort) {
        this.ort = ort;
    }
    public int getTicketanzahl() {
        return ticketanzahl;
    }
    public void setTicketanzahl(int ticketanzahl) {
        this.ticketanzahl = ticketanzahl;
    }
    public String getTicketoption() {
        return ticketoption;
    }
    public void setTicketoption(String ticketoption) {
        this.ticketoption = ticketoption;
    }

    
}
